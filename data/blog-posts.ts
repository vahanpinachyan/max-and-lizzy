function pick(locale: Locale, en: string, hy?: string, ru?: string): string {
  if (locale === "hy") return hy || en;
  if (locale === "ru") return ru || en;
  return en;
}

export function localizeBlogPost(post: BlogPost, locale: Locale = "en"): BlogPost {
  return {
    ...post,
    title: pick(locale, post.title, post.titleHy, post.titleRu),
    excerpt: pick(locale, post.excerpt, post.excerptHy, post.excerptRu),
    content: pick(locale, post.content, post.contentHy, post.contentRu),
    coverImageAlt: pick(locale, post.coverImageAlt, post.coverImageAltHy, post.coverImageAltRu),
    metaDescription: pick(locale, post.metaDescription, post.metaDescriptionHy, post.metaDescriptionRu),
    tags: locale === "hy" ? post.tagsHy ?? post.tags : locale === "ru" ? post.tagsRu ?? post.tags : post.tags,
  };
}

export function getBlogPost(slug: string) {
  return blogPosts.find((p) => p.slug === slug);
}

export function getRecentBlogPosts(limit = 3) {
  return [...blogPosts]
    .sort((a, b) => new Date(b.date).getTime() - new Date(a.date).getTime())
    .slice(0, limit);
}
